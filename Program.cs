using System;
using System.Collections.Generic;

class Комп'ютер
{
    public string IpАдреса { get; set; }
    public int Потужність { get; set; }
    public string ТипОС { get; set; }

    public void ВідправитиДані(Комп'ютер отримувач, string дані)
    {
        Console.WriteLine($"Відправлено дані з {IpАдреса} до {отримувач.IpАдреса}: {дані}");
    }
}

class Сервер : Комп'ютер, IConnectable
{
    public void Підключитися()
    {
        Console.WriteLine($"Сервер {IpАдреса} підключився до мережі.");
    }

    public void Відключитися()
    {
        Console.WriteLine($"Сервер {IpАдреса} відключився від мережі.");
    }
}

class РобочаСтанція : Комп'ютер, IConnectable
{
    public void Підключитися()
    {
        Console.WriteLine($"Робоча станція {IpАдреса} підключилася до мережі.");
    }

    public void Відключитися()
    {
        Console.WriteLine($"Робоча станція {IpАдреса} відключилася від мережі.");
    }
}

class Маршрутизатор : Комп'ютер, IConnectable
{
    public void ПідключитиКомп'ютер(Комп'ютер комп)
    {
        Console.WriteLine($"Маршрутизатор {IpАдреса} підключив комп'ютер {комп.IpАдреса} до мережі.");
    }

    public void ВідключитиКомп'ютер(Комп'ютер комп)
    {
        Console.WriteLine($"Маршрутизатор {IpАдреса} відключив комп'ютер {комп.IpАдреса} від мережі.");
    }
}

interface IConnectable
{
    void Підключитися();
    void Відключитися();
}

class Мережа
{
    private List<Комп'ютер> комп'ютери;

    public Мережа()
    {
        комп'ютери = new List<Комп'ютер>();
    }

    public void ДодатиКомп'ютер(Комп'ютер комп)
    {
        комп'ютери.Add(комп);
        комп.Підключитися();
    }

    public void ВидалитиКомп'ютер(Комп'ютер комп)
    {
        комп'ютери.Remove(комп);
        комп.Відключитися();
    }
}

class Program
{
    static void Main()
    {
        Мережа мережа = new Мережа();

        Сервер сервер1 = new Сервер { IpАдреса = "192.168.1.1", Потужність = 1000, ТипОС = "Windows Server" };
        РобочаСтанція робочаСтанція1 = new РобочаСтанція { IpАдреса = "192.168.1.2", Потужність = 500, ТипОС = "Windows" };

        мережа.ДодатиКомп'ютер(сервер1);
        мережа.ДодатиКомп'ютер(робочаСтанція1);

        сервер1.ВідправитиДані(робочаСтанція1, "Важливі дані для робочої станції.");

        мережа.ВидалитиКомп'ютер(робочаСтанція1);
    }
}
